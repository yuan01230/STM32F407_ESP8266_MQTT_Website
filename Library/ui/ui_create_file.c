/* ============================================================
 * Create File Module (ui_create_file.c)
 * - 负责 KEY2 创建“测试专用.txt”
 * - 负责写入 RTC + 中英混合内容并显示结果页
 * ============================================================ */

extern RTC_HandleTypeDef hrtc;

/* -------------------- static 前置声明区 -------------------- */
static void UI_Create_ShowPreview(const char *rtc_line, const uint8_t *result_line, uint8_t show_content);

/**
 * @file ui_create_file.c
 * @brief KEY2 创建文件页面模块
 * @details
 * 负责主页面按下 KEY2 后的完整流程：
 * 1. 读取 RTC 时间
 * 2. 在 SD 根目录创建“测试专用.txt”
 * 3. 写入 RTC + 中英文内容
 * 4. 在 LCD 显示结果页并延时返回
 */

/**
 * @brief 显示创建文件结果页
 * @param rtc_line RTC 时间文本（ASCII，可为空）
 * @param result_line 结果文本（GB2312 字节串）
 * @param show_content 1=显示写入预览，0=仅显示结果
 * @retval 无
 * @details
 * 功能：在 LCD 显示“创建成功/失败”结果与内容预览。
 * 输入：创建流程产生的结果信息。
 * 输出：结果页面渲染，停留 3 秒后返回调用方流程。
 */
static void UI_Create_ShowPreview(const char *rtc_line, const uint8_t *result_line, uint8_t show_content)
{
    /* 结果页固定布局：标题 -> 结果 -> 预览 -> 底部返回提示 */
    BACK_COLOR = WHITE;
    LCD_Clear(WHITE);
    FRONT_COLOR = BLUE;
    LCD_ShowString(8, 8, 304, 16, 16, (uint8_t *)"Create File Result");
    LCD_DrawLine(0, 28, 319, 28);

    FRONT_COLOR = BLACK;
    if (rtc_line != NULL)
    {
        LCD_ShowString(8, 44, 304, 16, 16, (uint8_t *)rtc_line);
    }
    LCD_ShowTextMixed(8, 72, 304, 16, result_line);

    if (show_content)
    {
        FRONT_COLOR = GREEN;
        UI_LCD_ShowUtf8(8, 132, 304, 16, "写入预览:");
        FRONT_COLOR = BLACK;
        UI_LCD_ShowUtf8(8, 174, 304, 16, "文件: 测试专用.txt");
        UI_LCD_ShowUtf8(8, 202, 304, 16, "内容: 中文 + English + RTC");
        UI_LCD_ShowUtf8(8, 230, 304, 16, "提示: 在列表页按 KEY2 打开验证");
    }

    FRONT_COLOR = BLUE;
    LCD_DrawLine(0, 430, 319, 430);
    UI_LCD_ShowUtf8(8, 446, 304, 16, "3秒后回到主页面");
    HAL_Delay(3000);
}

/**
 * @brief 创建测试文件并写入内容
 * @param 无
 * @retval 无
 * @details
 * 功能：在 SD 卡根目录创建“测试专用.txt”并写入测试文本。
 * 输入：RTC 当前时间、预设中英文文本。
 * 输出：文件创建结果页显示（成功/失败）。
 * 流程：
 * 1. 校验 SD 挂载状态；
 * 2. 读取 RTC 并生成首行时间文本；
 * 3. 路径 UTF-8 -> GB2312 转换后打开文件；
 * 4. 依次写入时间、中文行、英文行；
 * 5. 显示创建结果页面。
 */
void UI_CreateTestFileAndShow(void)
{
    FIL file;
    FRESULT fres;
    UINT bw = 0;
    char line_time[64];
    char fail_line[64];
    static const char zh_line1_utf8[] = "中文行1: 这是由 KEY2 按键创建的测试文件。\r\n";
    static const char zh_line2_utf8[] = "中文行2: 文件用于 SD 读写与显示功能验证。\r\n";
    static const char en_line1[] = "English line 1: This file is generated by KEY2.\r\n";
    static const char en_line2[] = "English line 2: Include RTC timestamp and mixed text content.\r\n";
    static const char en_line3[] = "English line 3: You can reopen this file in list page to verify.\r\n";
    static const char ok_utf8[] = "创建成功";
    static const char no_sd_utf8[] = "创建失败: SD not mounted";
    uint8_t path_gbk[128];
    uint8_t zh_line1_gbk[200];
    uint8_t zh_line2_gbk[200];
    uint8_t ok_gbk[64];
    uint8_t no_sd_gbk[96];
    RTC_TimeTypeDef sTime;
    RTC_DateTypeDef sDate;

    /* 前置条件：需要已挂载 SD */
    if (!g_sd_mounted)
    {
        UI_TextUtf8ToGb2312(no_sd_utf8, no_sd_gbk, (uint16_t)sizeof(no_sd_gbk));
        UI_Create_ShowPreview(NULL, no_sd_gbk, 0U);
        return;
    }

    /* 读取当前 RTC 并写入首行时间 */
    HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);
    HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);
    snprintf(line_time, sizeof(line_time), "RTC: 20%02u-%02u-%02u %02u:%02u:%02u\r\n",
             (unsigned int)sDate.Year, (unsigned int)sDate.Month, (unsigned int)sDate.Date,
             (unsigned int)sTime.Hours, (unsigned int)sTime.Minutes, (unsigned int)sTime.Seconds);

    /* 路径从 UTF-8 转 GB2312，确保 FatFs 可以正确识别中文文件名 */
    if (!UI_TextUtf8ToGb2312(TEST_FILE_PATH_UTF8, path_gbk, (uint16_t)sizeof(path_gbk)))
    {
        UI_Create_ShowPreview(line_time, (const uint8_t *)"Path convert fail", 0U);
        return;
    }

    fres = f_open(&file, (const char *)path_gbk, FA_CREATE_ALWAYS | FA_WRITE);
    if (fres != FR_OK)
    {
        snprintf(fail_line, sizeof(fail_line), "Open fail, code=%d", (int)fres);
        UI_Create_ShowPreview(line_time, (const uint8_t *)fail_line, 0U);
        return;
    }

    /* 中文内容先转码，再与英文内容一起写入文件 */
    UI_TextUtf8ToGb2312(zh_line1_utf8, zh_line1_gbk, (uint16_t)sizeof(zh_line1_gbk));
    UI_TextUtf8ToGb2312(zh_line2_utf8, zh_line2_gbk, (uint16_t)sizeof(zh_line2_gbk));

    /* 按“时间 -> 中文 -> 英文”顺序写入，便于读取验证 */
    fres = f_write(&file, line_time, (UINT)strlen(line_time), &bw);
    if (fres == FR_OK) fres = f_write(&file, zh_line1_gbk, (UINT)strlen((const char *)zh_line1_gbk), &bw);
    if (fres == FR_OK) fres = f_write(&file, zh_line2_gbk, (UINT)strlen((const char *)zh_line2_gbk), &bw);
    if (fres == FR_OK) fres = f_write(&file, en_line1, (UINT)strlen(en_line1), &bw);
    if (fres == FR_OK) fres = f_write(&file, en_line2, (UINT)strlen(en_line2), &bw);
    if (fres == FR_OK) fres = f_write(&file, en_line3, (UINT)strlen(en_line3), &bw);
    (void)f_close(&file);

    if (fres != FR_OK)
    {
        snprintf(fail_line, sizeof(fail_line), "Write fail, code=%d", (int)fres);
        UI_Create_ShowPreview(line_time, (const uint8_t *)fail_line, 1U);
        return;
    }

    /* 成功提示也走统一转码路径，保持显示链路一致 */
    UI_TextUtf8ToGb2312(ok_utf8, ok_gbk, (uint16_t)sizeof(ok_gbk));
    UI_Create_ShowPreview(line_time, ok_gbk, 1U);
}
