#ifndef _DHT11_H_
#define _DHT11_H_

#include "main.h"

// ============================================================================
// 引脚定义
// ============================================================================
#define DHT11_PORT      GPIOG
#define DHT11_PIN       GPIO_PIN_9

// ============================================================================
// 数据结构体
// ============================================================================
typedef struct
{
    uint8_t  humi_int;     // 湿度的整数部分
    uint8_t  humi_dec;     // 湿度的的小数部分
    uint8_t  temp_int;     // 温度的整数部分
    uint8_t  temp_dec;     // 温度的小数部分
    uint8_t  checksum;     // 校验和
} DHT11_Data_t;

// ============================================================================
// 函数声明
// ============================================================================

/**
 * @brief 初始化 DHT11
 * @note  实际上是初始化 GPIO 引脚
 */
void DHT11_Init(void);

/**
 * @brief 从 DHT11 读取温湿度数据
 * @param data: 指向 DHT11_Data_t 结构体的指针，用于存储读取的数据
 * @return 0: 成功, 1: 失败 (校验和错误或无响应)
 */
uint8_t DHT11_ReadData(DHT11_Data_t *data);

#endif //DHT11_H
